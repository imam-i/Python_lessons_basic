def my_sum(n):
       result = 0
       while n:
              result += n % 10
              n //= 10
       return result

file3 = open('./number', 'r', encoding='UTF-8')
number3 = int(file3.read())
number_max3 = 0
result3 = 0
while number3:
       _result3 = number3 % 100000

       if _result3 == 99999:
              result3 = _result3
              break

       _sum3 = my_sum(_result3)
       if number_max3 < _sum3:
              number_max3 = _sum3
              result3 = _result3

       number3 //= 10
file3.close()

